# STRING METHODS
text = ' Hello.mofos.this.is.aadarsh.here'
print(text.strip())  # Removes spaces from the beginning of the string
print(text.lower())  # Removes all the caps from the string
print(text.upper())  # Makes all the letter in the string CAPITAL
print(text.split('.'))  # Splits the string wrt '.' and puts them into a list
